static const uint8_t Rcon[11] = {0x8d,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};

#define xt(x) (((x)<<1)^((((x)>>7)&1)*0x1b))
#define EMPTY_HASH 0x956669ADu

static void KeyExpansion(uint8_t *rk, const uint8_t *key) {
    int i,j; uint8_t t[4];
    for(i=0;i<8;i++){rk[i*4]=key[i*4];rk[i*4+1]=key[i*4+1];rk[i*4+2]=key[i*4+2];rk[i*4+3]=key[i*4+3];}
    for(i=8;i<60;i++){
        for(j=0;j<4;j++) t[j]=rk[(i-1)*4+j];
        if(i%8==0){uint8_t k=t[0];t[0]=sbox[t[1]]^Rcon[i/8];t[1]=sbox[t[2]];t[2]=sbox[t[3]];t[3]=sbox[k];}
        else if(i%8==4){for(j=0;j<4;j++)t[j]=sbox[t[j]];}
        for(j=0;j<4;j++) rk[i*4+j]=rk[(i-8)*4+j]^t[j];
    }
}

static uint8_t mul(uint8_t x,uint8_t y){
    return (((y&1)*x)^((y>>1&1)*xt(x))^((y>>2&1)*xt(xt(x)))^
            ((y>>3&1)*xt(xt(xt(x))))^((y>>4&1)*xt(xt(xt(xt(x))))));
}

static void aes256_decrypt(const uint8_t *key, const uint8_t *in, uint8_t *out) {
    uint8_t rk[240], s[4][4]; int i,j,r;
    KeyExpansion(rk, key);
    for(i=0;i<4;i++) for(j=0;j<4;j++) s[i][j]=in[i*4+j];
    for(i=0;i<4;i++) for(j=0;j<4;j++) s[i][j]^=rk[14*16+i*4+j];
    for(r=13;r>=1;r--){
        uint8_t t;
        t=s[3][1];s[3][1]=s[2][1];s[2][1]=s[1][1];s[1][1]=s[0][1];s[0][1]=t;
        t=s[0][2];s[0][2]=s[2][2];s[2][2]=t;t=s[1][2];s[1][2]=s[3][2];s[3][2]=t;
        t=s[0][3];s[0][3]=s[1][3];s[1][3]=s[2][3];s[2][3]=s[3][3];s[3][3]=t;
        for(i=0;i<4;i++) for(j=0;j<4;j++) s[i][j]=rsbox[s[i][j]];
        for(i=0;i<4;i++) for(j=0;j<4;j++) s[i][j]^=rk[r*16+i*4+j];
        for(i=0;i<4;i++){
            uint8_t a=s[i][0],b=s[i][1],c=s[i][2],d=s[i][3];
            s[i][0]=mul(a,0x0e)^mul(b,0x0b)^mul(c,0x0d)^mul(d,0x09);
            s[i][1]=mul(a,0x09)^mul(b,0x0e)^mul(c,0x0b)^mul(d,0x0d);
            s[i][2]=mul(a,0x0d)^mul(b,0x09)^mul(c,0x0e)^mul(d,0x0b);
            s[i][3]=mul(a,0x0b)^mul(b,0x0d)^mul(c,0x09)^mul(d,0x0e);
        }
    }
    uint8_t t;
    t=s[3][1];s[3][1]=s[2][1];s[2][1]=s[1][1];s[1][1]=s[0][1];s[0][1]=t;
    t=s[0][2];s[0][2]=s[2][2];s[2][2]=t;t=s[1][2];s[1][2]=s[3][2];s[3][2]=t;
    t=s[0][3];s[0][3]=s[1][3];s[1][3]=s[2][3];s[2][3]=s[3][3];s[3][3]=t;
    for(i=0;i<4;i++) for(j=0;j<4;j++) s[i][j]=rsbox[s[i][j]];
    for(i=0;i<4;i++) for(j=0;j<4;j++) s[i][j]^=rk[i*4+j];
    for(i=0;i<4;i++) for(j=0;j<4;j++) out[i*4+j]=s[i][j];
}

static inline uint32_t wang(uint32_t h) {
    h += ~(h<<15); h ^= (h>>10); h += (h<<3);
    h ^= (h>>6);   h += ~(h<<11); h ^= (h>>16);
    return h;
}

static inline uint8_t dup_nibble(char c) { uint8_t d=c-'0'; return (d<<4)|d; }
static inline uint8_t last_nibble(char c) { uint8_t d=c-'0'; return ((d+1)<<4)|d; }

static uint32_t encode_group(const char *g, int len) {
    uint8_t b[4];
    if (len == 4) {
        b[0]=dup_nibble(g[0]); b[1]=dup_nibble(g[1]);
        b[2]=dup_nibble(g[2]); b[3]=dup_nibble(g[3]);
    } else {
        b[0] = len==1 ? last_nibble(g[0]) : dup_nibble(g[0]);
        b[1] = len==1 ? 0xFF : (len==2 ? last_nibble(g[1]) : dup_nibble(g[1]));
        b[2] = len<=2 ? 0xFF : last_nibble(g[2]);
        b[3] = 0xEF;
    }
    return ((uint32_t)b[0]<<24)|((uint32_t)b[1]<<16)|((uint32_t)b[2]<<8)|b[3];
}

static void pin_to_key_raw(const char *pin, int pinlen, uint8_t *key_raw) {
    uint32_t h[4]; int pos=0;
    for(int g=0;g<4;g++){
        int rem=pinlen-pos;
        if(rem<=0){ h[g]=EMPTY_HASH; }
        else { int gl=rem>4?4:rem; h[g]=wang(encode_group(pin+pos,gl)); pos+=gl; }
    }
    for(int g=0;g<4;g++){
        key_raw[g*4+0]=(h[g]>>24)&0xFF; key_raw[g*4+1]=(h[g]>>16)&0xFF;
        key_raw[g*4+2]=(h[g]>>8)&0xFF;  key_raw[g*4+3]= h[g]&0xFF;
    }
    memset(key_raw+16, 0xFF, 16);
}

static void transform_key(const uint8_t *kr, uint8_t *ak) {
    for(int i=0;i<16;i++) ak[i]=kr[15-i];
    for(int i=0;i<16;i++) ak[16+i]=kr[31-i];
}

