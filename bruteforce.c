static inline int check_dec(const uint8_t *d) {
    return d[0]==0x20&&d[1]==0x49&&d[2]==0x4E&&d[3]==0x49&&
           d[16]==0x20&&d[17]==0x49&&d[18]==0x4E&&d[19]==0x49;
}

static void decrypt_pin(const char *pin, int pinlen, uint8_t *dec) {
    uint8_t key_raw[32], aes_key[32];
    pin_to_key_raw(pin, pinlen, key_raw);
    transform_key(key_raw, aes_key);
    aes256_decrypt(aes_key, ENC, dec);
    aes256_decrypt(aes_key, ENC+16, dec+16);
    aes256_decrypt(aes_key, ENC+32, dec+32);
}

static int test_kdf(void) {
    static const uint8_t expected[16] = {
        0x79,0x3E,0x76,0xBA, 0xA7,0xFE,0x65,0x51,
        0xD9,0xEA,0x50,0xB8, 0x95,0x66,0x69,0xAD
    };
    uint8_t key_raw[32];
    pin_to_key_raw("1012330102", 10, key_raw);
    if(memcmp(key_raw, expected, 16)!=0) return 0;
    return 1;
}

static int test_aes(void) {
    uint8_t dec[48];
    decrypt_pin("1012330102", 10, dec);
    if(!(dec[0]==0x20&&dec[1]==0x49&&dec[2]==0x4E&&dec[3]==0x49&&dec[4]==0x64)) return 0;
    if(!(dec[16]==0x20&&dec[17]==0x49&&dec[18]==0x4E&&dec[19]==0x49)) return 0;
    return 1;
}

static uint64_t ipow10(int n){uint64_t r=1;for(int i=0;i<n;i++)r*=10;return r;}

static void idx_to_pin(uint64_t idx,int len,char *out){
    out[len]='\0';
    for(int i=len-1;i>=0;i--){out[i]='0'+(idx%10);idx/=10;}
}

static void brute_force(int min_len, int max_len, int threads) {
    int found_global=0;
    char found_pin[32]={0};
    uint8_t found_dec[48]={0};
    omp_set_num_threads(threads);

    for(int len=min_len; len<=max_len && !found_global; len++){
        uint64_t total=ipow10(len);

        #pragma omp parallel shared(found_global,found_pin,found_dec)
        {
            char pin[32]; uint8_t dec[48];

            #pragma omp for schedule(dynamic,50000)
            for(uint64_t idx=0; idx<total; idx++){
                if(found_global) continue;
                idx_to_pin(idx, len, pin);
                decrypt_pin(pin, len, dec);

                if(check_dec(dec)){
                    #pragma omp critical
                    { if(!found_global){
                        found_global=1;
                        memcpy(found_pin,pin,len+1);
                        memcpy(found_dec,dec,48);
                    }}
                }
            }
        }
    }
}

int main(int argc, char **argv) {
    int min_len = argc>1 ? atoi(argv[1]) : 4;
    int max_len = argc>2 ? atoi(argv[2]) : 12;
    int threads = argc>3 ? atoi(argv[3]) : 12;

    if(!test_kdf()) return 1;
    if(!test_aes()) return 1;

    brute_force(min_len, max_len, threads);
    return 0;
}